package at.jayden.project.basics.Handy;

public class SimCard {

    private final int id;
    private final int number;

    public SimCard(int id, int number) {
        this.id = id;
        this.number = number;
    }

    public int getId() {
        return id;
    }

    public int getNumber() {
        return number;
    }


}
